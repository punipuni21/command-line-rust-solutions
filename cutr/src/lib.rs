pub fn get_args() -> MyResult<Config> {
    let matches = Command::new("curt")
        .version("0.1.0")
        .author("ryo")
        .about("Rust cut")
        .arg(
            Arg::new("bytes")
                .value_name("BYTES")
                .short('b')
                .long("bytes")
                .help("Selected bytes")
                .conflicts_with_all(&["fields", "chars"])
                .num_args(0..),
        )
        .arg(
            Arg::new("chars")
                .value_name("CHARS")
                .short('c')
                .long("chars")
                .help("Selected characters")
                .conflicts_with_all(&["fields", "bytes"])
                .num_args(0..),
        )
        .arg(
            Arg::new("delimiter")
                .value_name("DELIMITER")
                .short('d')
                .long("delim")
                .help("Field delimiter")
                .default_value("\t")
                .num_args(0..=1),
        )
        .arg(
            Arg::new("fields")
                .value_name("FIELDS")
                .short('f')
                .long("fields")
                .help("Selected fields")
                .conflicts_with_all(&["bytes", "chars"])
                .num_args(0..),
        )
        .arg(
            Arg::new("files")
                .value_name("FILE")
                .help("Input files")
                .default_value("-")
                .num_args(1..),
        )
        .get_matches();

    let files: Vec<String> = matches
        .get_many::<String>("files")
        .unwrap()
        .map(|s| s.to_string())
        .collect();

    let delimiter = matches.get_one::<String>("delimiter").unwrap();
    let delim_bytes = delimiter.as_bytes();
    if delim_bytes.len() != 1 {
        return Err(From::from(format!(
            "--delim \"{}\" must be a single byte",
            delimiter
        )));
    }

    let fields = matches
        .get_one("fields")
        .cloned()
        .map(|v: String| parse_pos(v))
        .transpose()?;

    let bytes = matches
        .get_one("bytes")
        .cloned()
        .map(|v: String| parse_pos(v))
        .transpose()?;

    let chars = matches
        .get_one("chars")
        .cloned()
        .map(|v: String| parse_pos(v))
        .transpose()?;

    let extract = if let Some(field_pos) = fields {
        Fields(field_pos)
    } else if let Some(byte_pos) = bytes {
        Bytes(byte_pos)
    } else if let Some(char_pos) = chars {
        Chars(char_pos)
    } else {
        return Err(From::from("Must have --fields, --bytes, or --chars"));
    };

    Ok(Config {
        files,
        delimiter: *delim_bytes.first().unwrap(),
        extract,
    })
}

fn parse_index(input: String) -> Result<usize, String> {
    let value_error = || format!("illegal list value: \"{}\"", input);
    input
        .starts_with('+')
        .then(|| Err(value_error()))
        .unwrap_or_else(|| {
            input
                .parse::<NonZeroUsize>()
                .map(|n| usize::from(n) - 1)
                .map_err(|_| value_error())
        })
}

fn parse_pos(range: String) -> MyResult<PositionList> {
    let range_re = Regex::new(r"^(\d+)-(\d+)$").unwrap();
    range
        .split(',')
        .into_iter()
        .map(|val| {
            parse_index(val.to_string()).map(|n| n..n + 1).or_else(|e| {
                range_re.captures(val).ok_or(e).and_then(|captures| {
                    let n1 = parse_index(captures[1].to_string())?;
                    let n2 = parse_index(captures[2].to_string())?;
                    if n1 >= n2 {
                        return Err(format!(
                            "First number in range ({}) \
            must be lower than second number ({})",
                            n1 + 1,
                            n2 + 1
                        ));
                    }
                    Ok(n1..n2 + 1)
                })
            })
        })
        .collect::<Result<_, _>>()
        .map_err(From::from)
}

